	private static int getCharIdx(int c)
	{
		int cl = Character.toLowerCase(c);
		int charIdx = cl - 'a';
		if (charIdx >= numLetters)
			charIdx = -1;
		return charIdx;
	}

	private static double compareBigramDist(String text, double[] languageDist)
	{
		int[] dist = new int[numBigrams];
		int n = 0;
		int curCharIdx = -1;
		for (int i = 0; i < text.length(); i++)
		{
			int prevCharIdx = curCharIdx;
			curCharIdx = getCharIdx(text.charAt(i));
			if ((prevCharIdx >= 0) && (curCharIdx >= 0))
			{
				dist[prevCharIdx * numLetters + curCharIdx]++;
				n++;
			}
		}

		if (n == 0)
			return 0.0;

		double sumSqDiff = 0.0;
		for (int i = 0; i < numBigrams; i++)
			sumSqDiff += Math.pow(((double)dist[i] / n) - languageDist[i], 2.0);
		return sumSqDiff;
	}

